# enquanto : while
# enquanto veraddeiro (validado e retornando true, faça algo)

i = 1
while i <= 50
    puts "jenny - #{i}"
    i += 1 # vai executar até chegar em 50

end



# ate que : until
# enquanto for falso

until i <= 1
    puts "sou falso - #{i}"
    i -= 1 # vai executar até chegar em 1

end

puts "fim"